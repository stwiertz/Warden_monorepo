// ---------------------------------------------------------------------------
// Manifest modifier — permissions + <service> (idempotent).
// ---------------------------------------------------------------------------
function withManifestMod(config) {
  return withAndroidManifest(config, (config) => {
    const manifest = config.modResults.manifest;

    manifest["uses-permission"] = manifest["uses-permission"] ?? [];
    for (const perm of FGS_PERMISSIONS) {
      const exists = manifest["uses-permission"].some(
        (p) => p.$?.["android:name"] === perm
      );
      if (!exists) {
        manifest["uses-permission"].push({ $: { "android:name": perm } });
      }
    }

    const application = AndroidConfig.Manifest.getMainApplicationOrThrow(
      config.modResults
    );
    application.service = application.service ?? [];
    const serviceExists = application.service.some(
      (s) => s.$?.["android:name"] === SERVICE_NAME
    );
    if (!serviceExists) {
      application.service.push({
        $: {
          "android:name": SERVICE_NAME,
          // dataSync matches the FOREGROUND_SERVICE_DATA_SYNC permission
          // (architecture.md:817).
          "android:foregroundServiceType": "dataSync",
          "android:exported": "false",
          // Keep the FGS running after the launcher task is swiped away — the
          // exact J2 backgrounding case.
          "android:stopWithTask": "false",
        },
      });
    }

    return config;
  });
}

// ---------------------------------------------------------------------------
// Kotlin file emission (withDangerousMod — no @expo/config-plugins helper
// exists for arbitrary Kotlin files). Overwrite-on-prebuild; idempotent.
// ---------------------------------------------------------------------------
function withKotlinFiles(config) {
  return withDangerousMod(config, [
    "android",
    (config) => {
      const javaDir = path.join(
        config.modRequest.platformProjectRoot,
        "app",
        "src",
        "main",
        "java",
        PACKAGE_PATH
      );
      fs.mkdirSync(javaDir, { recursive: true });
      fs.writeFileSync(path.join(javaDir, "WardenProcessingService.kt"), SERVICE_KT);
      fs.writeFileSync(path.join(javaDir, "WardenProcessingModule.kt"), MODULE_KT);
      fs.writeFileSync(path.join(javaDir, "WardenProcessingPackage.kt"), PACKAGE_KT);
      return config;
    },
  ]);
}

// ---------------------------------------------------------------------------
// Register WardenProcessingPackage() in MainApplication.kt getPackages().
// MainApplication.kt is itself prebuild-generated; it shares the team.warden.mobile
// package so no import is needed. Idempotent — skips if already registered.
// ---------------------------------------------------------------------------
function withPackageRegistration(config) {
  return withMainApplication(config, (config) => {
    let src = config.modResults.contents;
    if (src.includes("WardenProcessingPackage()")) {
      return config;
    }
    // Expo SDK 54 MainApplication.kt exposes getPackages() as
    //   PackageList(this).packages.apply { /* add(MyReactNativePackage()) */ }
    // Inside the apply block the receiver is the MutableList, so a bare
    // `add(...)` registers our package. Anchor on the autolink comment marker.
    const anchor = "// add(MyReactNativePackage())";
    if (src.includes(anchor)) {
      src = src.replace(
        anchor,
        `${anchor}\n              // Story 1.2 (BF-5) — foreground service bridge (with-foreground-service.js)\n              add(WardenProcessingPackage())`
      );
    } else {
      throw new Error(
        "[with-foreground-service] could not find the autolink anchor in MainApplication.kt — " +
          "Expo SDK MainApplication shape changed; update the plugin."
      );
    }
    config.modResults.contents = src;
    return config;
  });
}

function withForegroundService(config) {
  return withRunOnce(config, {
    plugin: (config) =>
      withPlugins(config, [
        withManifestMod,
        withKotlinFiles,
        withPackageRegistration,
      ]),
    name: "with-foreground-service",
  });
}

module.exports = withForegroundService;
