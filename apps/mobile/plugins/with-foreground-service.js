// Story 1.2 (BF-5) — Foreground Service Expo config plugin.
//
// Architecture-bound choice (option a): a custom @expo/config-plugins plugin
// that injects an Android Foreground Service hosting the main JS context, so
// `runProcessingPipeline` survives backgrounding (J2). NOT expo-task-manager —
// FFmpeg/OpenCV JSI bindings cannot cross the headless context boundary
// (architecture.md:811).
//
// The plugin is Android-only by construction: `withAndroidManifest` and the
// android-scoped `withDangerousMod` / `withMainApplication` modifiers no-op on
// iOS. iOS Phase 2 ships BGTaskScheduler via a sibling plugin (architecture.md:881).
//
// What it does:
//   1. AndroidManifest — adds FOREGROUND_SERVICE, FOREGROUND_SERVICE_DATA_SYNC,
//      POST_NOTIFICATIONS permissions + the <service> declaration (idempotent).
//   2. Emits three Kotlin source files into the prebuild-generated app tree:
//      - WardenProcessingService.kt  (the FGS — keeps process alive, renders the
//        sticky "Analyse en cours…" notification from the JS-pushed stage).
//      - WardenProcessingModule.kt   (RN native module: start(sessionId) /
//        updateStage(stage) / stop()).
//      - WardenProcessingPackage.kt  (ReactPackage exposing the module).
//   3. Registers WardenProcessingPackage() in MainApplication.kt getPackages().
//
// No `props` surface: service name, package, FGS type, channel id, and the
// notification copy are all architecturally-bound constants. Premature
// configurability is anti-pattern (Story 1.1 "do NOT" precedent).

const {
  withAndroidManifest,
  withDangerousMod,
  withMainApplication,
  withPlugins,
  withRunOnce,
  AndroidConfig,
} = require("@expo/config-plugins");
const fs = require("fs");
const path = require("path");

const PACKAGE = "team.warden.mobile";
const PACKAGE_PATH = PACKAGE.split(".").join("/");

const SERVICE_NAME = ".WardenProcessingService";
const FGS_PERMISSIONS = [
  "android.permission.FOREGROUND_SERVICE",
  "android.permission.FOREGROUND_SERVICE_DATA_SYNC",
  "android.permission.POST_NOTIFICATIONS",
];

// ---------------------------------------------------------------------------
// Kotlin source — the FGS itself.
// Sole jobs: (a) keep the process alive, (b) display the notification with the
// current pipeline stage. NO Firebase/FFmpeg/OpenCV — the real pipeline runs in
// the JS context via processingPipeline.ts.
//
// Stage delivery: PUSHED from JS via the bridge `updateStage(stage)`, which
// re-delivers onStartCommand on the already-running service with a fresh
// `stage` extra. react-native-mmkv@3.3.3 builds only the MMKV C++ Core (no
// `com.tencent.mmkv.MMKV` Kotlin API), so a Kotlin-side MMKV poll is not
// available; the JS pipeline is the single source of truth for the stage.
// (Story 1.2 — AC3 mechanism adapted; user-approved 2026-06-11.)
// ---------------------------------------------------------------------------
const SERVICE_KT = `package team.warden.mobile

import android.app.Notification
import android.app.NotificationChannel
import android.app.NotificationManager
import android.app.Service
import android.content.Intent
import android.content.pm.ServiceInfo
import android.os.Build
import android.os.IBinder
import android.util.Log
import androidx.core.app.NotificationCompat

// Story 1.2 (BF-5) — Foreground Service hosting the main JS context.
// Generated by apps/mobile/plugins/with-foreground-service.js. Do not edit the
// prebuild-generated copy; edit the plugin template.
class WardenProcessingService : Service() {

    override fun onCreate() {
        super.onCreate()
        ensureChannel()
    }

    override fun onStartCommand(intent: Intent?, flags: Int, startId: Int): Int {
        // Stage is pushed from JS (bridge updateStage); each push re-enters here
        // on the already-running service. A null stage (initial start) shows the
        // "Préparation…" fallback — no leak, no crash.
        if (intent == null) {
            Log.w(TAG, "onStartCommand: null intent (system redelivery); showing fallback text")
        }
        val text = stageToText(intent?.getStringExtra("stage"))
        val notification = buildNotification(text)
        // Android 14+ (UPSIDE_DOWN_CAKE = 34) requires the typed 3-arg overload.
        if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.UPSIDE_DOWN_CAKE) {
            startForeground(NOTIF_ID, notification, ServiceInfo.FOREGROUND_SERVICE_TYPE_DATA_SYNC)
        } else {
            startForeground(NOTIF_ID, notification)
        }
        // Stop is delivered as a start-command (action extra) so it queues FIFO
        // behind any in-flight start/updateStage command — startForeground above
        // has always run before the service tears itself down, closing the
        // "startForegroundService() did not then call startForeground()" crash
        // window (code-review patch, 2026-06-11).
        if (intent?.getStringExtra("action") == "stop") {
            stopForeground(STOP_FOREGROUND_REMOVE)
            stopSelf()
            return START_NOT_STICKY
        }
        // START_NOT_STICKY (code-review deviation from AC3's START_STICKY,
        // user-approved 2026-06-11): a sticky restart resurrects the service
        // with a DEAD JS context — no pipeline, no stop() caller — leaving a
        // permanent orphan notification. Recovery after an OS kill is the MMKV
        // checkpoint-resume path on app relaunch (J2 Tier 2), not a service
        // restart the JS side can never reclaim.
        return START_NOT_STICKY
    }

    // Android 15+ caps dataSync FGS time (6h per 24h, cumulative). When the
    // budget is exhausted the system calls onTimeout and ANR-crashes the app
    // if the service does not stop promptly. Stop gracefully instead: the JS
    // pipeline keeps running while the app is foregrounded, and the MMKV
    // checkpoint covers recovery if the OS later kills the backgrounded
    // process (code-review patch, 2026-06-11).
    override fun onTimeout(startId: Int) {
        Log.w(TAG, "dataSync FGS time budget exhausted; stopping (checkpoint-resume covers recovery)")
        stopForeground(STOP_FOREGROUND_REMOVE)
        stopSelf()
    }

    override fun onDestroy() {
        stopForeground(STOP_FOREGROUND_REMOVE)
        super.onDestroy()
    }

    override fun onBind(intent: Intent?): IBinder? = null

    private fun stageToText(stage: String?): String = when (stage) {
        "keyframes" -> "Extraction des images-clés"
        "detection" -> "Analyse des images"
        "segmentation" -> "Segmentation des parties"
        "results" -> "Extraction des miniatures"
        else -> {
            // Defensive (AC3): an unknown non-null stage key means the TS↔Kotlin
            // stage contract drifted — warn loudly, fall back gracefully.
            if (stage != null) {
                Log.w(TAG, "unknown stage key '" + stage + "'; showing fallback text")
            }
            "Préparation…"
        }
    }

    private fun buildNotification(text: String): Notification =
        NotificationCompat.Builder(this, CHANNEL_ID)
            .setContentTitle("Analyse en cours…")
            .setContentText(text)
            .setSmallIcon(android.R.drawable.stat_notify_sync)
            .setOngoing(true)
            .setOnlyAlertOnce(true)
            .build()

    private fun ensureChannel() {
        if (Build.VERSION.SDK_INT < Build.VERSION_CODES.O) return
        val nm = getSystemService(NOTIFICATION_SERVICE) as NotificationManager
        if (nm.getNotificationChannel(CHANNEL_ID) != null) return
        val channel = NotificationChannel(
            CHANNEL_ID, "Traitement vidéo", NotificationManager.IMPORTANCE_LOW
        ).apply {
            description = "Notifications affichées pendant l'analyse vidéo en arrière-plan."
            setShowBadge(false)
        }
        nm.createNotificationChannel(channel)
    }

    companion object {
        private const val TAG = "WardenProcessing"
        private const val NOTIF_ID = 1001
        private const val CHANNEL_ID = "processing"
    }
}
`;

// ---------------------------------------------------------------------------
// Kotlin source — the legacy RN native module (start/stop bridge).
// Works under bridgeless/new-arch via RN's interop layer. Accessed from JS as
// NativeModules.WardenProcessing.
// ---------------------------------------------------------------------------
const MODULE_KT = `package team.warden.mobile

import android.content.Intent
import androidx.core.content.ContextCompat
import com.facebook.react.bridge.Promise
import com.facebook.react.bridge.ReactApplicationContext
import com.facebook.react.bridge.ReactContextBaseJavaModule
import com.facebook.react.bridge.ReactMethod

// Story 1.2 (BF-5) — JS↔Kotlin bridge for the foreground service.
// Generated by apps/mobile/plugins/with-foreground-service.js.
class WardenProcessingModule(reactContext: ReactApplicationContext) :
    ReactContextBaseJavaModule(reactContext) {

    override fun getName(): String = "WardenProcessing"

    @ReactMethod
    fun start(sessionId: String, promise: Promise) {
        try {
            val intent = Intent(reactApplicationContext, WardenProcessingService::class.java)
            intent.putExtra("sessionId", sessionId)
            ContextCompat.startForegroundService(reactApplicationContext, intent)
            promise.resolve(null)
        } catch (e: Exception) {
            promise.reject("WARDEN_FGS_START_FAILED", e)
        }
    }

    // Push the current pipeline stage to the running service; re-delivers
    // onStartCommand with the new stage extra so the notification text updates.
    @ReactMethod
    fun updateStage(stage: String, promise: Promise) {
        try {
            val intent = Intent(reactApplicationContext, WardenProcessingService::class.java)
            intent.putExtra("stage", stage)
            ContextCompat.startForegroundService(reactApplicationContext, intent)
            promise.resolve(null)
        } catch (e: Exception) {
            promise.reject("WARDEN_FGS_UPDATE_FAILED", e)
        }
    }

    // Stop is delivered through startForegroundService as an "action=stop"
    // start-command so it queues FIFO behind any in-flight start/updateStage —
    // the service always reaches startForeground before stopping itself (see
    // WardenProcessingService.onStartCommand). If the start-command path is
    // rejected (e.g. Android 12+ background-start restriction edge), fall back
    // to a direct stopService, which is always permitted.
    @ReactMethod
    fun stop(promise: Promise) {
        try {
            val intent = Intent(reactApplicationContext, WardenProcessingService::class.java)
            intent.putExtra("action", "stop")
            try {
                ContextCompat.startForegroundService(reactApplicationContext, intent)
            } catch (e: Exception) {
                reactApplicationContext.stopService(intent)
            }
            promise.resolve(null)
        } catch (e: Exception) {
            promise.reject("WARDEN_FGS_STOP_FAILED", e)
        }
    }
}
`;

